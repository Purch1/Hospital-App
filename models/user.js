const mongoose          = require('mongoose')
const Schema            = mongoose.Schema

const userSchema = new Schema({
    name: {
        type: String,
        required: true,
        minlength: 5,
        maxlength: 50
    },
    email: {
        type: String,
        required: true,
        minlength: 5,
        maxlength: 255,
        unique: true
    },
    phone: {
        type: String,
        required: true,
        minlength: 5,
        maxlength: 50
    },
    password: {
        type: String,
        required: true,
        minlength: 5,
        maxlength: 1024
    },
    resetPassword: String, 
    expireToken: Date

                
}, {timestamps: true}

)


const User = mongoose.model('User', userSchema)
module.exports = User